package com.shop.internetshop.product.model.enums;

public enum ProductStatus {
    /**
     * Aktywny - dostępny w sklepie
     */
    ACTIVE,

    /**
     * Nieaktywny - ukryty w sklepie
     */
    INACTIVE,

    /**
     * Wersja robocza - nie opublikowany
     */
    DRAFT,

    /**
     * Zakończony - nie jest już dostępny
     */
    DISCONTINUED
}
